//Write a program to check if a given number is odd or even.
import java.util.*;
public class Second {

	public static void main(String[] args) {
		Scanner sc = new Scanner(System.in);
		int n=sc.nextInt();
		if(n%2==0)
			System.out.println("number is even");
		else 
			System.out.println("number is odd");
	}

}
